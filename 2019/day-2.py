def getResult(arry, noun, verb):
    i = 0
    op = 0
    length = len(arry)
    arry[1] = noun
    arry[2] = verb

    while i < length and op != 99:
        op = arry[i]
        if op != 99:
            firstPos = arry[i+1]
            secondPos = arry[i+2]
            destination = arry[i+3]

            first = arry[firstPos]
            second = arry[secondPos]

            result = 0
            if op == 1:
                result = first + second
            
            if op == 2:
                result = first * second

            arry[destination] = result
        
        i = i + 4
    
    return arry[0]

